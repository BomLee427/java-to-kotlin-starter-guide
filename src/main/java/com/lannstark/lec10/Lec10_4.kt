package com.lannstark.lec10

/**
 * 4. 상속 관련 키워드 4가지
 */

/**
 * 1. final
 * - override를 할 수 없게 한다. 명시하지 않아도 default로 보이지 않게 존재한다.
 */

/**
 * 2. open
 * - override가 가능하다.
 * - override를 해도 되고, 하지 않아도 된다.
 */

/**
 * 3. abstract
 * - override가 가능하다.
 * - 반드시 override해야 한다.
 */

/**
 * 4. override
 * - 상위 타입을 override하고 있다는 의미.
 * - 어노테이션이 아니라 키워드로 사용하는 것이기 때문에 반드시 붙여야 한다.
 */